    private void eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarMouseClicked
        String result = "";
        int cont = 1;
        for (int i = 0; i < song.size(); i++) {
            Cancion c = song.get(i);
            result += cont + " - " + c.toString();
            cont++;
            result += "\n";
        }
        String num = JOptionPane.showInputDialog(null, result + "\n"
                + "Ingrese el indice de la cancion que desea eliminar: ");
        int indice = Integer.parseInt(num);
        if (indice <= song.size()) {
            cont = indice;
            indice = indice - 1;
            JOptionPane.showMessageDialog(null, "Se ha eliminado la canción:\n" + cont + ". " + song.get(indice).toString());
            song.remove(indice);
        } else {
            JOptionPane.showMessageDialog(null, "Indice no valido");
        }

    }//GEN-LAST:event_eliminarMouseClicked


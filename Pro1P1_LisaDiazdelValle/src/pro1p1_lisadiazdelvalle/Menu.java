    public String decToBin(int dec, String bin) {
        if (dec == 0 || dec == 1) {
            return (char) (dec + 48) + bin;
        } else {
            return decToBin(dec / 2, (dec % 2) + bin);
        }
    }

    public int binariodecimal(String binario, int index, int resp) {
        if (index == 0) {
            int base = Integer.parseInt(Character.toString(binario.charAt(binario.length() - 1 - index))) * 2;
            return resp + (int) Math.pow(base, index);
        } else {
            int base = Integer.parseInt(Character.toString(binario.charAt(binario.length() - 1 - index))) * 2;
            return binariodecimal(binario, index - 1, resp + (int) Math.pow(base, index));

        }
    }

    public String impresion(int[][] matrix, int r_index, int c_index, String output) {
        if (r_index >= 0 && c_index >= 0) {
            output = '[' + Integer.toString(matrix[r_index][c_index]) + ']' + output;
        }

        if (r_index == 0 && c_index == 0) {
            return output;
        } else {
            if (c_index == 0) {
                output = '\n' + output;
                return impresion(matrix, r_index - 1, matrix[0].length - 1, output);
            } else {
                return impresion(matrix, r_index, c_index - 1, output);
            }
        }
    }

    public int[][] llenado(int[][] matrix, int f_index, int c_index) {
        if (f_index >= 0 && c_index >= 0) {
            matrix[f_index][c_index] = aleatorio.nextInt(9);
        }
        if (f_index == 0 && c_index == 0) {
            return matrix;
        } else {
            if (c_index == 0) {
                return llenado(matrix, f_index - 1, matrix[0].length - 1);
            } else {
                return llenado(matrix, f_index, c_index - 1);
            }
        }
    }

    public int rowProduct(int[][] matriz1, int[][] matriz2, int fila, int columna, int index, int resultado) {
        if (index == matriz1[0].length - 1) {
            resultado += matriz1[fila][index] * matriz2[index][columna];
            return resultado;
        } else {
            resultado += matriz1[fila][index] * matriz2[index][columna];
            return rowProduct(matriz1, matriz2, fila, columna, index + 1, resultado);

        }
    }

    public int[][] multiplicacion(int[][] matriz_a, int[][] matriz_b, int[][] matriz_c, int indexfila, int indexcolumna) {
        if (indexfila >= 0 && indexcolumna >= 0) {
            matriz_c[indexfila][indexcolumna] = rowProduct(matriz_a, matriz_b, indexfila, indexcolumna, 0, 0);
        }
        if (indexfila == 0 && indexcolumna == 0) {
            return matriz_c;
        } else {
            if (indexcolumna == 0) {
                return multiplicacion(matriz_a, matriz_b, matriz_c, indexfila - 1, matriz_c[0].length - 1);
            } else {
                return multiplicacion(matriz_a, matriz_b, matriz_c, indexfila, indexcolumna - 1);
            }
        }
    }

